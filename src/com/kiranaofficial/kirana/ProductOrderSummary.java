package com.kiranaofficial.kirana;

public class ProductOrderSummary {
	String productName;
	String productQty;
	String productTotalCost;
	
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductQty() {
		return productQty;
	}
	public void setProductQty(String productQty) {
		this.productQty = productQty;
	}
	public String getProductTotalCost() {
		return productTotalCost;
	}
	public void setProductTotalCost(String productTotalCost) {
		this.productTotalCost = productTotalCost;
	}
	
}
